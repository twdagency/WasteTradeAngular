/**
 * Offline script: Generate TypeScript field constants from existing markdown docs.
 * Use this when you can't connect to Salesforce directly.
 * For production use, prefer: node scripts/export-field-configs.js (connects to SF)
 *
 * Run with: node scripts/generate-ts-from-docs.js
 */

const fs = require('fs');
const path = require('path');

const OBJECTS = [
    { mdFile: 'Account.md', tsPrefix: 'Account', sfName: 'Account' },
    { mdFile: 'Contact.md', tsPrefix: 'Contact', sfName: 'Contact' },
    { mdFile: 'Lead.md', tsPrefix: 'Lead', sfName: 'Lead' },
    { mdFile: 'Sales_Listing.md', tsPrefix: 'SalesListing', sfName: 'Sales_Listing__c' },
    { mdFile: 'Wanted_Listings.md', tsPrefix: 'WantedListing', sfName: 'Wanted_Listings__c' },
    { mdFile: 'Offers.md', tsPrefix: 'Offers', sfName: 'Offers__c' },
    { mdFile: 'Haulage_Offers.md', tsPrefix: 'HaulageOffers', sfName: 'Haulage_Offers__c' },
    { mdFile: 'Haulage_Loads.md', tsPrefix: 'HaulageLoads', sfName: 'Haulage_Loads__c' },
    { mdFile: 'Document.md', tsPrefix: 'Document', sfName: 'Document__c' },
    { mdFile: 'Content_Document.md', tsPrefix: 'ContentDocument', sfName: 'ContentDocument' },
    { mdFile: 'Content_Version.md', tsPrefix: 'ContentVersion', sfName: 'ContentVersion' },
    { mdFile: 'Content_Document_Link.md', tsPrefix: 'ContentDocumentLink', sfName: 'ContentDocumentLink' },
];

function toKebabCase(str) {
    return str
        .replace(/([a-z])([A-Z])/g, '$1-$2')
        .replace(/[_\s]+/g, '-')
        .toLowerCase();
}

function sanitizeIdentifier(value) {
    let id = value
        .replace(/[^a-zA-Z0-9_]/g, '_')
        .replace(/_+/g, '_')
        .replace(/^_|_$/g, '');
    if (/^[0-9]/.test(id)) {
        id = '_' + id;
    }
    if (!id) {
        id = '_empty';
    }
    return id;
}

/**
 * Parse field names and types from markdown table rows
 * Matches: | Label | `FieldName` | type | ... |
 */
function parseFieldsFromMarkdown(content) {
    const fields = [];
    const fieldRegex = /\|\s*[^|]*\|\s*`([^`]+)`\s*\|\s*(\w+)\s*\|/g;
    let match;
    while ((match = fieldRegex.exec(content)) !== null) {
        fields.push({ name: match[1], type: match[2] });
    }
    return fields;
}

/**
 * Parse picklist values from markdown
 * Matches sections like: ### Label (`FieldName`) ... ```\n- "Value"\n```
 */
function parsePicklistsFromMarkdown(content) {
    const picklists = {};
    const sectionRegex = /###\s+[^(]*\(`([^`]+)`\)\s*\n[\s\S]*?```\n([\s\S]*?)```/g;
    let match;
    while ((match = sectionRegex.exec(content)) !== null) {
        const fieldName = match[1];
        const valuesBlock = match[2];
        const values = [];
        const valueRegex = /-\s*"([^"]+)"(\s*\(default\))?/g;
        let valMatch;
        while ((valMatch = valueRegex.exec(valuesBlock)) !== null) {
            values.push({ value: valMatch[1], isDefault: !!valMatch[2] });
        }
        if (values.length > 0) {
            picklists[fieldName] = values;
        }
    }
    return picklists;
}

function generateTsFile(fields, picklists, tsPrefix, sfName) {
    const timestamp = new Date().toISOString();
    const lines = [];

    lines.push('/**');
    lines.push(` * AUTO-GENERATED by scripts/generate-ts-from-docs.js`);
    lines.push(` * Do not edit manually. Re-run the export script to update.`);
    lines.push(` *`);
    lines.push(` * Salesforce Object: ${sfName}`);
    lines.push(` * Total Fields: ${fields.length}`);
    lines.push(` * Generated: ${timestamp}`);
    lines.push(` */`);
    lines.push('');

    // Field constants
    lines.push(`/** Field API names for ${sfName} */`);
    lines.push(`export const ${tsPrefix}Fields = {`);
    for (const field of fields) {
        lines.push(`    ${field.name}: '${field.name}',`);
    }
    lines.push('} as const;');
    lines.push('');

    // Field name union type
    lines.push(`/** Union type of all ${tsPrefix} field API names */`);
    lines.push(`export type ${tsPrefix}FieldName = typeof ${tsPrefix}Fields[keyof typeof ${tsPrefix}Fields];`);
    lines.push('');

    // Picklist enums
    const picklistFieldNames = Object.keys(picklists);
    if (picklistFieldNames.length > 0) {
        lines.push('// --- Picklist Values ---');
        lines.push('');

        for (const fieldName of picklistFieldNames) {
            const values = picklists[fieldName];

            const constName =
                tsPrefix +
                fieldName
                    .replace(/__c$/, '')
                    .replace(/_/g, ' ')
                    .replace(/\b\w/g, (c) => c.toUpperCase())
                    .replace(/\s/g, '') +
                'Values';

            const seenKeys = new Set();
            const entries = [];
            for (const val of values) {
                let key = sanitizeIdentifier(val.value);
                if (seenKeys.has(key)) {
                    key = key + '_' + seenKeys.size;
                }
                seenKeys.add(key);
                const escapedValue = val.value.replace(/'/g, "\\'");
                entries.push({ key, value: escapedValue });
            }

            lines.push(`/** Picklist values for ${fieldName} */`);
            lines.push(`export const ${constName} = {`);
            for (const entry of entries) {
                lines.push(`    ${entry.key}: '${entry.value}',`);
            }
            lines.push('} as const;');

            const typeName = constName.replace(/Values$/, 'Value');
            lines.push(`export type ${typeName} = typeof ${constName}[keyof typeof ${constName}];`);
            lines.push('');
        }
    }

    return lines.join('\n') + '\n';
}

function main() {
    const mdDir = path.join(__dirname, '..', 'docs', 'salesforce', 'fields');
    const tsDir = path.join(__dirname, '..', 'src', 'utils', 'salesforce', 'generated');

    if (!fs.existsSync(tsDir)) {
        fs.mkdirSync(tsDir, { recursive: true });
    }

    console.log('🔍 Generating TypeScript from existing markdown docs...\n');

    const tsExports = [];

    for (const obj of OBJECTS) {
        const mdPath = path.join(mdDir, obj.mdFile);
        if (!fs.existsSync(mdPath)) {
            console.log(`   ⏭️  ${obj.mdFile} not found, skipping`);
            continue;
        }

        const content = fs.readFileSync(mdPath, 'utf8');
        const fields = parseFieldsFromMarkdown(content);
        const picklists = parsePicklistsFromMarkdown(content);

        const tsContent = generateTsFile(fields, picklists, obj.tsPrefix, obj.sfName);
        const tsFilename = toKebabCase(obj.tsPrefix) + '-fields.generated.ts';
        const tsPath = path.join(tsDir, tsFilename);
        fs.writeFileSync(tsPath, tsContent, 'utf8');

        console.log(`   ✅ ${tsFilename} (${fields.length} fields, ${Object.keys(picklists).length} picklists)`);
        tsExports.push({ filename: tsFilename, prefix: obj.tsPrefix });
    }

    // Generate index.ts
    const indexLines = [
        '/**',
        ' * AUTO-GENERATED by scripts/generate-ts-from-docs.js',
        ' * Do not edit manually. Re-run the export script to update.',
        ` * Generated: ${new Date().toISOString()}`,
        ' */',
        '',
    ];
    for (const exp of tsExports) {
        const moduleName = exp.filename.replace('.ts', '');
        indexLines.push(`export * from './${moduleName}';`);
    }
    indexLines.push('');

    fs.writeFileSync(path.join(tsDir, 'index.ts'), indexLines.join('\n'), 'utf8');
    console.log(`\n✅ Generated index.ts (${tsExports.length} modules)`);
    console.log(`📁 Output: ${tsDir}`);
}

main();
