/**
 * Script to export all field configurations from Salesforce objects to:
 * 1. Markdown docs (docs/salesforce/fields/*.md)
 * 2. TypeScript constants (src/utils/salesforce/generated/*.generated.ts)
 *
 * The generated TS files provide compile-time safety — if a field is removed
 * from Salesforce, re-running this script drops it from the TS file, causing
 * compile errors in any code that references it.
 *
 * Run with: node scripts/export-field-configs.js
 */

const fs = require('fs');
const path = require('path');
const { createConnection } = require('./lib/salesforce-utils');
const { OBJECTS } = require('./lib/salesforce-config');

// Objects to export (with TS generation config)
const EXPORT_OBJECTS = [
    { name: OBJECTS.ACCOUNT, filename: 'Account.md', tsPrefix: 'Account' },
    { name: OBJECTS.CONTACT, filename: 'Contact.md', tsPrefix: 'Contact' },
    { name: OBJECTS.LEAD, filename: 'Lead.md', tsPrefix: 'Lead' },
    { name: OBJECTS.SALES_LISTING, filename: 'Sales_Listing.md', tsPrefix: 'SalesListing' },
    { name: OBJECTS.WANTED_LISTING, filename: 'Wanted_Listings.md', tsPrefix: 'WantedListing' },
    { name: OBJECTS.OFFERS, filename: 'Offers.md', tsPrefix: 'Offers' },
    { name: OBJECTS.HAULAGE_OFFERS, filename: 'Haulage_Offers.md', tsPrefix: 'HaulageOffers' },
    { name: OBJECTS.HAULAGE_LOADS, filename: 'Haulage_Loads.md', tsPrefix: 'HaulageLoads' },
    { name: OBJECTS.DOCUMENT, filename: 'Document.md', tsPrefix: 'Document' },
    { name: OBJECTS.CONTENT_DOCUMENT, filename: 'Content_Document.md', tsPrefix: 'ContentDocument' },
    { name: OBJECTS.CONTENT_VERSION, filename: 'Content_Version.md', tsPrefix: 'ContentVersion' },
    { name: OBJECTS.CONTENT_DOCUMENT_LINK, filename: 'Content_Document_Link.md', tsPrefix: 'ContentDocumentLink' },
];

/**
 * Convert a prefix like 'SalesListing' to kebab-case 'sales-listing'
 */
function toKebabCase(str) {
    return str
        .replace(/([a-z])([A-Z])/g, '$1-$2')
        .replace(/[_\s]+/g, '-')
        .toLowerCase();
}

/**
 * Sanitize a string into a valid JS identifier
 * Used for picklist value keys (field API names are already valid from SF)
 */
function sanitizeIdentifier(value) {
    let id = value
        .replace(/[^a-zA-Z0-9_]/g, '_') // replace non-alphanumeric with _
        .replace(/_+/g, '_')             // collapse multiple underscores
        .replace(/^_|_$/g, '');           // trim leading/trailing underscores
    if (/^[0-9]/.test(id)) {
        id = '_' + id;
    }
    if (!id) {
        id = '_empty';
    }
    return id;
}

/**
 * Generate TypeScript file content from Salesforce object describe result
 */
function generateTypeScriptFile(describe, tsPrefix, sfObjectName) {
    const timestamp = new Date().toISOString();
    const lines = [];

    // Header
    lines.push('/**');
    lines.push(` * AUTO-GENERATED by scripts/export-field-configs.js`);
    lines.push(` * Do not edit manually. Re-run the export script to update.`);
    lines.push(` *`);
    lines.push(` * Salesforce Object: ${sfObjectName} (${describe.label})`);
    lines.push(` * Total Fields: ${describe.fields.length}`);
    lines.push(` * Generated: ${timestamp}`);
    lines.push(` */`);
    lines.push('');

    // Field constants
    lines.push(`/** Field API names for ${describe.label} (${sfObjectName}) */`);
    lines.push(`export const ${tsPrefix}Fields = {`);
    for (const field of describe.fields) {
        lines.push(`    ${field.name}: '${field.name}',`);
    }
    lines.push('} as const;');
    lines.push('');

    // Field name union type
    lines.push(`/** Union type of all ${tsPrefix} field API names */`);
    lines.push(`export type ${tsPrefix}FieldName = typeof ${tsPrefix}Fields[keyof typeof ${tsPrefix}Fields];`);
    lines.push('');

    // Picklist enums
    const picklistFields = describe.fields.filter(
        (f) => f.type === 'picklist' && f.picklistValues && f.picklistValues.length > 0,
    );

    if (picklistFields.length > 0) {
        lines.push('// --- Picklist Values ---');
        lines.push('');

        for (const field of picklistFields) {
            const activeValues = field.picklistValues.filter((v) => v.active);
            if (activeValues.length === 0) continue;

            // Build a const name from field name: Account_Status__c -> AccountStatus
            const constName =
                tsPrefix +
                field.name
                    .replace(/__c$/, '')
                    .replace(/_/g, ' ')
                    .replace(/\b\w/g, (c) => c.toUpperCase())
                    .replace(/\s/g, '') +
                'Values';

            // Deduplicate keys (some picklists have values that sanitize to same identifier)
            const seenKeys = new Set();
            const entries = [];
            for (const val of activeValues) {
                let key = sanitizeIdentifier(val.value);
                if (seenKeys.has(key)) {
                    key = key + '_' + seenKeys.size;
                }
                seenKeys.add(key);
                // Escape single quotes in value
                const escapedValue = val.value.replace(/'/g, "\\'");
                entries.push({ key, value: escapedValue });
            }

            lines.push(`/** Picklist values for ${field.name} */`);
            lines.push(`export const ${constName} = {`);
            for (const entry of entries) {
                lines.push(`    ${entry.key}: '${entry.value}',`);
            }
            lines.push('} as const;');

            const typeName = constName.replace(/Values$/, 'Value');
            lines.push(`export type ${typeName} = typeof ${constName}[keyof typeof ${constName}];`);
            lines.push('');
        }
    }

    return lines.join('\n') + '\n';
}

/**
 * Generate markdown field table (unchanged from original)
 */
function generateFieldTable(fields) {
    let table = '| Field Label | API Name | Type | Required | Unique | External ID | Description |\n';
    table += '|------------|----------|------|----------|--------|-------------|-------------|\n';

    for (const field of fields) {
        const label = field.label || '';
        const name = `\`${field.name}\``;
        const type = field.type || '';
        const required = field.nillable === false ? '✓' : '';
        const unique = field.unique ? '✓' : '';
        const externalId = field.externalId ? '✓' : '';
        const description = (field.inlineHelpText || '').replace(/\n/g, ' ').substring(0, 50);

        table += `| ${label} | ${name} | ${type} | ${required} | ${unique} | ${externalId} | ${description} |\n`;
    }

    return table + '\n';
}

async function exportFieldConfigs() {
    console.log('🔍 Exporting Salesforce field configurations...\n');

    try {
        const conn = await createConnection();
        console.log('✅ Connected to Salesforce\n');

        // Output directories
        const mdOutputDir = path.join(__dirname, '..', 'docs', 'salesforce', 'fields');
        const tsOutputDir = path.join(__dirname, '..', 'src', 'utils', 'salesforce', 'generated');

        for (const dir of [mdOutputDir, tsOutputDir]) {
            if (!fs.existsSync(dir)) {
                fs.mkdirSync(dir, { recursive: true });
            }
        }

        const tsExports = []; // Track generated TS files for index

        for (const obj of EXPORT_OBJECTS) {
            try {
                console.log(`📦 Fetching ${obj.name}...`);

                const describe = await conn.sobject(obj.name).describe();

                // --- Generate Markdown (unchanged) ---
                let markdown = `# ${describe.label} (${obj.name})\n\n`;
                markdown += `**API Name:** \`${obj.name}\`\n\n`;
                markdown += `**Total Fields:** ${describe.fields.length}\n\n`;
                markdown += `---\n\n`;

                const standardFields = describe.fields.filter(
                    (f) => !f.name.endsWith('__c') && !f.name.endsWith('__r'),
                );
                const customFields = describe.fields.filter((f) => f.name.endsWith('__c'));
                const relationshipFields = describe.fields.filter((f) => f.name.endsWith('__r'));

                if (standardFields.length > 0) {
                    markdown += `## Standard Fields (${standardFields.length})\n\n`;
                    markdown += generateFieldTable(standardFields);
                }
                if (customFields.length > 0) {
                    markdown += `\n## Custom Fields (${customFields.length})\n\n`;
                    markdown += generateFieldTable(customFields);
                }
                if (relationshipFields.length > 0) {
                    markdown += `\n## Relationship Fields (${relationshipFields.length})\n\n`;
                    markdown += generateFieldTable(relationshipFields);
                }

                const picklistFields = describe.fields.filter(
                    (f) => f.type === 'picklist' && f.picklistValues && f.picklistValues.length > 0,
                );
                if (picklistFields.length > 0) {
                    markdown += `\n## Picklist Values\n\n`;
                    for (const field of picklistFields) {
                        markdown += `### ${field.label} (\`${field.name}\`)\n\n`;
                        const activeValues = field.picklistValues.filter((v) => v.active);
                        markdown += `**Active Values:** ${activeValues.length}\n\n`;
                        markdown += '```\n';
                        for (const val of activeValues) {
                            markdown += `- "${val.value}"${val.defaultValue ? ' (default)' : ''}\n`;
                        }
                        markdown += '```\n\n';
                    }
                }

                const mdPath = path.join(mdOutputDir, obj.filename);
                fs.writeFileSync(mdPath, markdown, 'utf8');
                console.log(`   ✅ MD  → ${obj.filename}`);

                // --- Generate TypeScript ---
                const tsContent = generateTypeScriptFile(describe, obj.tsPrefix, obj.name);
                const tsFilename = toKebabCase(obj.tsPrefix) + '-fields.generated.ts';
                const tsPath = path.join(tsOutputDir, tsFilename);
                fs.writeFileSync(tsPath, tsContent, 'utf8');
                console.log(`   ✅ TS  → ${tsFilename} (${describe.fields.length} fields)`);

                tsExports.push({ filename: tsFilename, prefix: obj.tsPrefix });
            } catch (err) {
                console.log(`   ❌ Error: ${err.message}`);
            }
        }

        // --- Generate index.ts ---
        const indexLines = [
            '/**',
            ' * AUTO-GENERATED by scripts/export-field-configs.js',
            ' * Do not edit manually. Re-run the export script to update.',
            ` * Generated: ${new Date().toISOString()}`,
            ' */',
            '',
        ];
        for (const exp of tsExports) {
            const moduleName = exp.filename.replace('.ts', '');
            indexLines.push(`export * from './${moduleName}';`);
        }
        indexLines.push('');

        const indexPath = path.join(tsOutputDir, 'index.ts');
        fs.writeFileSync(indexPath, indexLines.join('\n'), 'utf8');
        console.log(`\n✅ Generated index.ts (${tsExports.length} modules)`);

        console.log('\n✅ Export completed!');
        console.log(`📁 Markdown: ${mdOutputDir}`);
        console.log(`📁 TypeScript: ${tsOutputDir}`);
    } catch (error) {
        console.error('❌ Error:', error.message);
        process.exit(1);
    }
}

exportFieldConfigs();
